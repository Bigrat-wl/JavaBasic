package variables;

public class IntArithmetic {
    public static void main(String[] args) {
        // 移位运算
        int n = 7; // 00000000 00000000 00000000 00000111 = 7
        int a = n << 1; // 00000000 00000000 00000000 00001110 = 14
        int b = n << 2; // 00000000 00000000 00000000 00011100 = 28

        int n2 = 7;       // 00000000 00000000 00000000 00000111 = 7
        int a2 = n >> 1;  // 00000000 00000000 00000000 00000011 = 3
        int b2 = n >> 2;  // 00000000 00000000 00000000 00000001 = 1
        int c2 = n2 >> 3;  // 00000000 00000000 00000000 00000000 = 0

        int n3 = -536870912;
        int a3 = n >>> 1;  // 01110000 00000000 00000000 00000000 = 1879048192
        int b3 = n >>> 2;  // 00111000 00000000 00000000 00000000 = 939524096
        int c3 = n >>> 29; // 00000000 00000000 00000000 00000111 = 7
        int d3 = n >>> 31; // 00000000 00000000 00000000 00000001 = 1


        // 位运算 &和 |或 ~否 ^异或
        int aa = 0 & 1; // 0
        int ab = 0 | 1; // 0
        int ac = ~0; // 1
        int ad = 0 ^ 0; // 1
    }
}
